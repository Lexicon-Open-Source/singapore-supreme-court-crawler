package models

import "github.com/golang-module/carbon/v2"

type CrawlerResult struct {
	Url      string          `json:"url"`
	Metadata CrawlerMetadata `json:"metadata"`
}

type CrawlerMetadata struct {
	Title          string          `json:"title"`
	CatchWords     string          `json:"catch_words"`
	DecisionDate   carbon.DateTime `json:"decision_date"`
	CitationNumber string          `json:"citation_number"`
	CaseNumber     string          `json:"case_number"`
}
